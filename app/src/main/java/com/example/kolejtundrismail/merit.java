package com.example.kolejtundrismail;

class merit {

    String title ;
    String merit ;
    String desc ;

    public merit(String title, String merit, String desc) {
        this.title = title;
        this.merit = merit;
        this.desc = desc;
    }

    public merit() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getMerit() {
        return merit;
    }

    public void setMerit(String merit) {
        this.merit = merit;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }


}

